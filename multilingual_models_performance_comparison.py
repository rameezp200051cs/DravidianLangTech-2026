"""## 9. Set Learning Rate

"""

LEARNING_RATE = 2e-5

"""## 10. Calculate Optimal Max Length"""

# Load tokenizer to analyze text lengths
temp_tokenizer = AutoTokenizer.from_pretrained('xlm-roberta-base')

# Get token lengths for all texts
train_df['token_length'] = train_df['Text'].apply(
    lambda x: len(temp_tokenizer.encode(x, add_special_tokens=True))
)

# Calculate statistics
print("\nToken Length Statistics:")
print(train_df['token_length'].describe())

# Set max_length to cover 95% of texts
max_length = int(train_df['token_length'].quantile(0.95))

# Cap at 256
max_length = min(max_length, 256)

# Ensure at least 64
max_length = max(max_length, 64)

print(f"\nOptimal max_length selected: {max_length}")

pct_covered = (train_df['token_length'] <= max_length).mean() * 100
print(f"  Percentage of texts fully covered: {pct_covered:.2f}%")

train_df = train_df.drop('token_length', axis=1)

del temp_tokenizer

"""## 11. Define Metrics and Training Function"""

def compute_metrics(eval_pred):
    predictions, labels = eval_pred
    predictions = np.argmax(predictions, axis=1)

    precision, recall, f1, _ = precision_recall_fscore_support(
        labels, predictions, average='binary'
    )
    acc = accuracy_score(labels, predictions)

    return {
        'accuracy': acc,
        'f1': f1,
        'precision': precision,
        'recall': recall
    }

def train_and_evaluate_model(model_name, model_checkpoint):
    print(f"\n{'='*70}")
    print(f"TRAINING {model_name.upper()}")
    print(f"{'='*70}\n")

    # Load tokenizer and model
    tokenizer = AutoTokenizer.from_pretrained(model_checkpoint)
    model = AutoModelForSequenceClassification.from_pretrained(
        model_checkpoint,
        num_labels=2,
        id2label=id2label,
        label2id=label2id,
        ignore_mismatched_sizes=True  # For pretrained models with different labels
    )

    # Tokenization function
    def tokenize_function(examples):
        return tokenizer(
            examples['Text'],
            padding='max_length',
            truncation=True,
            max_length=max_length  # Using calculated optimal length
        )

    # Convert to HuggingFace datasets
    train_dataset = Dataset.from_pandas(train_data[['Text', 'label']])
    val_dataset = Dataset.from_pandas(val_data[['Text', 'label']])

    # Tokenize datasets
    train_dataset = train_dataset.map(tokenize_function, batched=True)
    val_dataset = val_dataset.map(tokenize_function, batched=True)

    # Data collator
    data_collator = DataCollatorWithPadding(tokenizer=tokenizer)

    # Optimized training arguments
    model_training_args = TrainingArguments(
        output_dir=f'./results/{model_name}',

        num_train_epochs=7,

        # Batch sizes
        per_device_train_batch_size=16,
        per_device_eval_batch_size=32,

        # Learning rate with warmup
        learning_rate=LEARNING_RATE,
        warmup_ratio=0.1,

        # Regularization
        weight_decay=0.01,
        max_grad_norm=1.0,

        # Evaluation and saving
        eval_strategy="epoch",
        save_strategy="epoch",
        load_best_model_at_end=True,
        metric_for_best_model="f1",
        greater_is_better=True,

        # Logging
        logging_dir=f'./logs/{model_name}',
        logging_steps=50,

        # Other
        push_to_hub=False,
        report_to="none",
        seed=42,
    )

    # Initialize Trainer
    trainer = Trainer(
        model=model,
        args=model_training_args,
        train_dataset=train_dataset,
        eval_dataset=val_dataset,
        data_collator=data_collator,
        compute_metrics=compute_metrics,
    )

    # Train the model
    print(f"\nStarting training...")
    trainer.train()

    # Evaluate on validation set
    print(f"\nEvaluating on validation set...")
    results = trainer.evaluate()

    print(f"\n{model_name.upper()} - Validation Results:")
    print(f"  Accuracy:  {results['eval_accuracy']:.4f}")
    print(f"  F1 Score:  {results['eval_f1']:.4f}")
    print(f"  Precision: {results['eval_precision']:.4f}")
    print(f"  Recall:    {results['eval_recall']:.4f}")

    return trainer, tokenizer, results

