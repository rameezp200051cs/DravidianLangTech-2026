def generate_predictions(model_name, trainer, tokenizer, test_df):
    """
    Generate predictions for test dataset
    """
    print(f"\nGenerating predictions for {model_name}")

    # Create test dataset
    test_dataset = Dataset.from_pandas(test_df[['Text']])

    # Tokenize
    def tokenize_function(examples):
        return tokenizer(
            examples['Text'],
            padding='max_length',
            truncation=True,
            max_length=max_length
        )

    test_dataset = test_dataset.map(tokenize_function, batched=True)

    # Get predictions
    predictions = trainer.predict(test_dataset)
    pred_labels = np.argmax(predictions.predictions, axis=1)
    pred_classes = [id2label[label] for label in pred_labels]

    # Create predictions dataframe
    predictions_df = test_df.copy()
    predictions_df['predicted_class'] = pred_classes

    return predictions_df

