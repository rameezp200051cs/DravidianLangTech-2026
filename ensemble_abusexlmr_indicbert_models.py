"""## 14. Ensemble Method 1: Hard Voting"""

def hard_voting(pred1, pred2):
    """Simple majority voting"""
    ensemble_preds = []
    for p1, p2 in zip(pred1, pred2):
        if p1 == p2:
            ensemble_preds.append(p1)
        else:
            # Tiebreaker: use first model's prediction
            ensemble_preds.append(p1)
    return np.array(ensemble_preds)

