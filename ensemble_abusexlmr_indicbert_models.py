"""## 16. Ensemble Method 3: Weighted Voting"""

def weighted_voting(probs1, probs2, weight1, weight2):
    """Weighted average based on model performance"""
    total_weight = weight1 + weight2
    w1 = weight1 / total_weight
    w2 = weight2 / total_weight

    weighted_probs = (probs1 * w1) + (probs2 * w2)
    ensemble_preds = np.argmax(weighted_probs, axis=-1)

    return ensemble_preds, weighted_probs

